package ibg;

public class Initials {
	/*main method is the entry point to any java program which is static so no need to create any object
	to main method and its public which can be called from anywhere and it doesn't return anything so return type is void*/
	public static void main(String[] args) {
		//printing initials on to console using '*' character
		System.out.println();
        System.out.println("      *               *     ***********");
        System.out.println("       *             *     *            ");
        System.out.println("        *           *      *            ");
        System.out.println("         *         *       *               ");
        System.out.println("          *       *        *              ");
        System.out.println("           *     *         *           ");
        System.out.println("            *   *          *               ");
        System.out.println("             * *           *               ");
        System.out.println("              *             ***********");
        System.out.println();
     }  // end main()
	}


